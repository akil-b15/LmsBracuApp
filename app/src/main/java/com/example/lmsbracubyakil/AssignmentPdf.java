package com.example.lmsbracubyakil;

public class AssignmentPdf {

    public String name;
    public String url;

    public AssignmentPdf() {
    }

    public AssignmentPdf(String name, String url) {
        this.name = name;
        this.url = url;
    }

    public String getName() {
        return name;
    }


    public String getUrl() {
        return url;
    }

}

package com.example.lmsbracubyakil;

public class SubmitAssignment {

    public String name;
    public String url;

    public SubmitAssignment() {
    }

    public SubmitAssignment(String name, String url) {
        this.name = name;
        this.url = url;
    }

    public String getName() {
        return name;
    }


    public String getUrl() {
        return url;
    }

}
